    private void BtnResultadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnResultadoActionPerformed
        PnlResultado.setVisible(true);
        int contador=0;
        try 
            {
                Estudiantes.registros();
                clv=Estudiantes.registros().getInt("Clavest");
                Notas.consultanota(clv);
                    for(int i=0; Notas.registros().next() ; i++)
                    {
                        float notas_base=Float.parseFloat(Notas.registros().getString("Nota"));
                        notas=notas+notas_base;
                        contador=contador+1;
                    }
                    
                    String nombre = Estudiantes.registros().getString("Nombre");
                    promedio= new ClsEstudiante();
                    float prom = promedio.promedio(notas,contador);
                    estado= new ClsEstudiante();
                    boolean est = estado.estado(prom);
                    respuesta= new ClsEstudiante();
                    String res = respuesta.toString(nombre, prom, est);
                    Notas.registros().close();
                  txtResultado.setText(res);
            } 
            catch (SQLException ex) 
            {
                Logger.getLogger(WinEstudiante.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_BtnResultadoActionPerformed

