    private void ArchBdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ArchBdActionPerformed
        File f = ArchBd.getSelectedFile();
        String BaseDatos= f.getAbsolutePath();
        try
        {
            Estudiantes =new ClsBdEstudiante(BaseDatos);
            Estudiantes.consulta("TbEstudiante");
            Notas =new ClsBdEstudiante(BaseDatos);
            Notas.consulta("TbNotas");
        }
        catch (SQLException ex)
        {
            Logger.getLogger(WinEstudiante.class.getName()).log(Level.SEVERE, null, ex);
        }
        ArchBd.setVisible(false);
    }//GEN-LAST:event_ArchBdActionPerformed

    private void BtnSiguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSiguienteActionPerformed
        try 
            {
                Estudiantes.siguiente();
                {
                    
                    txtClave.setText(Estudiantes.registros().getString("Clavest"));
                    txtNombre.setText(Estudiantes.registros().getString("Nombre"));
                    txtDireccion.setText(Estudiantes.registros().getString("Direccion"));
                    txtSexo.setText(Estudiantes.registros().getString("Sexo"));
                    clv=Estudiantes.registros().getInt("Clavest");
                    Notas.consultanota(clv);
                    CmbNotas.removeAllItems();
                    for(int i=0; Notas.registros().next() ; i++)
                    {
                        CmbNotas.addItem(Notas.registros().getString("Nota"));
                    }
                    Notas.registros().close();
                }   
            } 
            catch (SQLException ex) 
            {
                Logger.getLogger(WinEstudiante.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_BtnSiguienteActionPerformed

